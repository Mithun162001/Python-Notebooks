import streamlit as st

# Creating title
st.title("This is a title")            # displays the title

# Creating headers
st.header("This is a header")         # displays text in header formatting

# Creating markdown
st.markdown("This is a markdown")

# Creating a text
st.text("This is a text")

# Creating a success message
st.success("Success")

# Creating a warning message
st.warning("Warning")

# Creating a error message
st.error("This is a error")